package com.example.oauth.modals;

public class CreateIssueReturnModal {

    String id;
    String key;
    String self;
    public CreateIssueReturnModal() {
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    public String getKey() {
        return key;
    }
    public void setKey(String key) {
        this.key = key;
    }
    public String getSelf() {
        return self;
    }
    public void setSelf(String self) {
        this.self = self;
    }
}
